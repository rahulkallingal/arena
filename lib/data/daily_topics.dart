/// A curated pool of debate "topics of the day" — one for every day of the year.
///
/// Each is phrased as an open QUESTION ("debate this"), never as a stated fact,
/// so Arena never looks like it's pushing a claim as true — just inviting an
/// argument. Topics use SIMPLE, everyday words so anyone can understand and
/// join in, no matter their reading level or background. They are still written
/// to *tempt* a reply: easy dilemmas, "would you...", "is it okay to...".
///
/// Guard-rails kept across the whole list: no defamation of real living people,
/// nothing that incites hatred against any group, and no harmful/illegal
/// content. Conspiracies appear only as harmless debate prompts.
///
/// The app picks one per calendar day (see DailyTopicService). With 365 topics
/// the cycle is a full year before any topic repeats.
class DailyTopic {
  final String topic;
  final String category;
  const DailyTopic(this.topic, this.category);
}

